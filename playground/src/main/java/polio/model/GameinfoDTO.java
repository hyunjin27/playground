package polio.model;

public class GameinfoDTO {
	int g_no;
	String g_name;
	String g_genre;
	String g_company;
	String g_release;
	String g_photo;
	String g_detail;
	String reguser;
	
	public GameinfoDTO() {
	}

	public int getG_no() {
		return g_no;
	}

	public void setG_no(int g_no) {
		this.g_no = g_no;
	}

	public String getG_name() {
		return g_name;
	}

	public void setG_name(String g_name) {
		this.g_name = g_name;
	}

	public String getG_genre() {
		return g_genre;
	}

	public void setG_genre(String g_genre) {
		this.g_genre = g_genre;
	}

	public String getG_company() {
		return g_company;
	}

	public void setG_company(String g_company) {
		this.g_company = g_company;
	}

	public String getG_release() {
		return g_release;
	}

	public void setG_release(String g_release) {
		this.g_release = g_release;
	}

	public String getG_photo() {
		return g_photo;
	}

	public void setG_photo(String g_photo) {
		this.g_photo = g_photo;
	}

	public String getG_detail() {
		return g_detail;
	}

	public void setG_detail(String g_detail) {
		this.g_detail = g_detail;
	}

	public String getReguser() {
		return reguser;
	}

	public void setReguser(String reguser) {
		this.reguser = reguser;
	}

}
